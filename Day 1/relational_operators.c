#include<stdio.h>

int main() {
    printf("%d \n", 4==4); // Equal
    printf("%d \n", 4<3); // Less than
    printf("%d \n", 3<4); 
    printf("%d \n", 4<4);
    printf("%d \n", 4<=4); // Less than equal to
    printf("%d \n", 4>3); // Greater than
    printf("%d \n", 3>4);
    printf("%d \n", 4>4);
    printf("%d \n", 4>=4); // Greater than equal to
    printf("%d \n", 4!=4); // Not equal or NEGATION
    printf("%d \n", 3!=4); 

    return 0;
}